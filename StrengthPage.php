        <section class="types">
            <h2>Popular Strength Training Exercises</h2>
            <img class="strength-pic" src="images/strength.jpg">
            <ul>
                <li><strong>Squats</strong>: Effective for lower body strength and engaging core muscles.</li>
                <li><strong>Deadlifts</strong>: A compound exercise that targets multiple muscle groups, especially the back and legs.</li>
                <li><strong>Bench Press</strong>: Great for upper body strength, primarily targeting the chest, shoulders, and triceps.</li>
                <li><strong>Pull-Ups</strong>: Bodyweight exercise focusing on upper body strength, especially back and biceps.</li>
                <li><strong>Overhead Press</strong>: Targets shoulders and arms, contributing to overall upper body strength.</li>
                <li><strong>Lunges</strong>: Strengthens the lower body and improves balance and coordination.</li>
            </ul>
        </section>

